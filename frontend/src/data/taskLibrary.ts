const activeItems = (): TaskLibraryItem[] => TASK_LIBRARY.filter((t) => t.active);

export function getTasksByCategory(categoryKey: ProductCategoryKey): TaskLibraryItem[] {
  return activeItems().filter((t) => t.categoryKey === categoryKey).sort(bySortOrder);
}

export function getDailyTasks(): TaskLibraryItem[] {
  return activeItems().filter((t) => t.frequency === "daily").sort(bySortOrder);
}

export function getMonthlyTasks(): TaskLibraryItem[] {
  return activeItems().filter((t) => t.frequency === "monthly").sort(bySortOrder);
}

export function getCoreTasksByCategory(categoryKey: ProductCategoryKey): TaskLibraryItem[] {
  return getTasksByCategory(categoryKey).filter((t) => t.isCore);
}

/** Title for UI from current i18n language code (he / en / …). */
export function getTaskLibraryTitle(item: TaskLibraryItem, language: string): string {
  const core = (language || "he").split("-")[0]?.toLowerCase() ?? "he";
  if (core === "he") return item.titleHe;
  return item.titleEn;
}

/** Plain JSON for scripts / backend seeding. */
export function serializeTaskLibraryForExport(): TaskLibraryItem[] {
  return TASK_LIBRARY.map((row) => ({ ...row }));
}
